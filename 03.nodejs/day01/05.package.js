/*
  package.json 包描述文件
    name 包名：不能有中文、特殊字符等 
      作用；下载包时传入的名称
    version 版本号  x.x.x
    dependencies 生产依赖：运行包时需要使用的依赖
    devDependencies 开发依赖：构建包时需要使用依赖
    scripts 运行包的指令
      "start"  --> npm start
      "build"  --> npm run build

  创建package.json
    npm init 初始化一个package.json，里面需要自己详细配置
    npm init -y 初始化一个默认配置package.json，
*/